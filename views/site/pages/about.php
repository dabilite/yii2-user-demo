<h1>Yii2-user</h1>
<p>
    Most of web applications provide a way for users to register, log in or reset their forgotten passwords. Rather than
    re-implementing this on each application, you can use Yii2-user which is a flexible user management module for Yii2
    that handles common tasks such as registration, authentication and password retrieval.
</p>
<h3>Features:</h3>
<ul>
    <li>Registration, with an optional confirmation via email</li>
    <li>Password retrieval</li>
    <li>Account and profile management</li>
    <li>Console commands</li>
    <li>User management interface</li>
</ul>
<div class="alert alert-warning">
    Module is in initial development. Anything may change at any time.
</div>
